

function show() {
	var popup=document.getElementById("popup");
	var mask=document.getElementById("mask");
	popup.style.display="block";
	mask.style.display="block";

}

function exit() {
	var popup=document.getElementById("popup");
	var mask=document.getElementById("mask");
	popup.style.display="none";
	mask.style.display="none";
	
}